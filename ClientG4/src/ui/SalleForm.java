    private void bnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedMachine = jTable1.getSelectedRow();
        try {
            if (selectedMachine != -1 ){
                String reference = txtPrix.getText();
                String marque = txtMarque.getText();
                double prix = Double.parseDouble(txtPrix.getText());
                int idMachine = Integer.parseInt(jTable1.getModel().getValueAt(selectedMachine,0).toString());
                Machine newMachine = new Machine(reference,marque , prix);
                newMachine.setId(idMachine);
                dao.update(newMachine);
                load();
            }

        }
        catch (RemoteException ex) {
            Logger.getLogger(MachineForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_bnUpdateActionPerformed


